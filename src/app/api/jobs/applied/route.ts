import { NextResponse } from "next/server";

import { getAccountType } from "@/lib/auth/account-type";
import { fetchAppliedJobs } from "@/lib/jobs/fetch-applied-jobs";
import { createClient } from "@/lib/supabase/server";

export async function GET() {
  const supabase = await createClient();
  const {
    data: { user },
  } = await supabase.auth.getUser();

  if (!user) {
    return NextResponse.json(
      { success: false, error: "Authentication required." },
      { status: 401 },
    );
  }

  const accountType = await getAccountType(supabase, user);

  if (accountType !== "candidate") {
    return NextResponse.json(
      { success: false, error: "Candidate access required." },
      { status: 403 },
    );
  }

  const result = await fetchAppliedJobs(supabase);

  if (result.error) {
    return NextResponse.json(
      { success: false, error: result.error },
      { status: 500 },
    );
  }

  return NextResponse.json({
    success: true,
    jobIds: result.applications.map((application) => application.job.id),
    applications: result.applications,
  });
}
